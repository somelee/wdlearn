#include <iostream>
using std::cout;
using std::endl;

int main()
{
	bool a = 100;
	bool b = -100;
	bool c = 0;
	bool d = true;
	bool f = false;
	cout << "a = " << a << endl;
	cout << "b = " << b << endl;
	cout << "c = " << c << endl;
	cout << "d = " << d << endl;
	cout << "f = " << f << endl;
	cout << "sizeof(bool) = " << sizeof(bool) << endl;
	cout << "sizeof(char) = " << sizeof(char) << endl;
	return 0;
}

